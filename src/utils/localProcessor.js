export function translateTextToLocal(text) {
    let translated = text.toLowerCase();
    
    // Replace words with Malayalam equivalents
    Object.entries(malayalamDictionary).forEach(([en, ml]) => {
        if (ml) { // Only replace if there's a translation
            const regex = new RegExp(`\\b${en}\\b`, 'gi');
            translated = translated.replace(regex, ml);
        }
    });
    
    // Clean up extra spaces and format
    translated = translated.replace(/\s+/g, ' ').trim();
    
    // Add Malayalam sentence structure markers
    translated = translated.replace(/([.!?])/g, '$1 ');
    
    return translated;
}

export function generateExamNotes(text) {
    const lines = text.split('\n').filter(line => line.trim());
    const translatedLines = lines.map(line => translateTextToLocal(line.trim()));
    const notes = translatedLines.map(line => `• ${line}`).join('\n');
    return `പരീക്ഷാ കുറിപ്പുകൾ:\n${notes}`;
}

export function generateSummary(text) {
    const sentences = text.match(/[^.!?]+[.!?]+/g) || [];
    const summary = sentences.slice(0, 3).join('');
    const translatedSummary = translateTextToLocal(summary);
    return `സംഗ്രഹം:\n${translatedSummary}`;
}

export function generateQuestions(text) {
    const translatedText = translateTextToLocal(text);
    return `പരീക്ഷാ ചോദ്യങ്ങൾ:\n\n2 മാർക്ക് ചോദ്യങ്ങൾ:\n1. ${translatedText.slice(0, 50)}... എന്താണ്?\n2. ഇതിന്റെ പ്രധാന സവിശേഷതകൾ എന്തെല്ലാം?\n\n5 മാർക്ക് ചോദ്യം:\n${translatedText.slice(0, 100)}... വിശദീകരിക്കുക.\n\n10 മാർക്ക് ചോദ്യം:\n${translatedText.slice(0, 150)}... സമഗ്രമായി വിശദീകരിക്കുക.`;
}

export function generateFlashcards(text) {
    const words = text.split(' ').filter(w => w.length > 5);
    const translatedWords = words.slice(0, 5).map(word => translateTextToLocal(word));
    const cards = translatedWords.map((word, i) => 
        `കാർഡ് ${i + 1}:\nമുൻവശം: ${word}\nപിൻവശം: ${word}-ന്റെ നിർവചനം`
    ).join('\n\n');
    return `പഠന കാർഡുകൾ:\n${cards}`;
}

export function processDocumentLocally(pages, mode) {
    return pages.map(page => {
        const text = page.trim();
        if (!text) return '';
        
        switch (mode) {
            case 'translate': return translateTextToLocal(text);
            case 'exam': return generateExamNotes(text);
            case 'summary': return generateSummary(text);
            case 'explain': return `വിശദീകരണം:\n${translateTextToLocal(text)}`;
            case 'questions': return generateQuestions(text);
            case 'flashcards': return generateFlashcards(text);
            default: return text;
        }
    });
}
